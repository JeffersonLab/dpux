package org.jlab.epsci.dpux.core;


public class CDefinedComponent {
    private String name = "undefined";
    private String type = "undefined";
    private String subType = "undefined";
    private int id = -1;
    private String description = "undefined";

    public CDefinedComponent(String n, String t, String st, int od, String d){
        name = n;
        type = t;
        subType = st;
        id = od;
        description = d;
    }
    public CDefinedComponent(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSubType() {
        return subType;
    }

    public void setSubType(String subType) {
        this.subType = subType;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int od) {
        id = od;
    }

    @Override
    public String toString() {
        return "CDefinedComponent{" +
                "name='" + name + '\'' +
                ", type='" + type + '\'' +
                ", subType='" + subType + '\'' +
                ", id=" + id +
                ", description='" + description + '\'' +
                '}';
    }
}
